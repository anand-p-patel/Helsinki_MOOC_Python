while True :
    #user defined string
    user_word = str(input("Please type in a string:"))
    #conditional checks for letters
    if "a" in user_word :
        print("a found")
    else:
        print("a not found")
    if "e" in user_word:
        print("e found")
    else:
        print("e not found")
    if "o" in user_word:
        print("o found")
    else:
        print("o not found")
    #stop loop
    break